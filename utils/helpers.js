// This is a helper so users will have varied colors to their name when they post updates

function stringToColor(str) {
    if (!str) return '#000000'; // Default color if string (black)
    let hash = 0;
    for (let i = 0; i < str.length; i++) {
        hash = str.charCodeAt(i) + ((hash << 5) - hash);
        hash = hash & hash;
    }
    let color = '#';
    for (let i = 0; i < 3; i++) {
        const value = (hash >> (i * 8)) & 0xFF;
        color += ('00' + value.toString(16)).slice(-2);
    }
    return color;
}

module.exports = { stringToColor };